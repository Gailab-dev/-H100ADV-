package com.minwon.vo;

public class DepartmentVO {
	private int dp_id;
	private String dp_name;
	private int dp_depth;
	private int dp_pid;
	private int dp_rank;
	
	public int getDp_id() {
		return dp_id;
	}
	public void setDp_id(int dp_id) {
		this.dp_id = dp_id;
	}
	public String getDp_name() {
		return dp_name;
	}
	public void setDp_name(String dp_name) {
		this.dp_name = dp_name;
	}
	public int getDp_depth() {
		return dp_depth;
	}
	public void setDp_depth(int dp_depth) {
		this.dp_depth = dp_depth;
	}
	public int getDp_pid() {
		return dp_pid;
	}
	public void setDp_pid(int dp_pid) {
		this.dp_pid = dp_pid;
	}
	public int getDp_rank() {
		return dp_rank;
	}
	public void setDp_rank(int dp_rank) {
		this.dp_rank = dp_rank;
	}
}

